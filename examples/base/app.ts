axios({ // 这个例子相对下个例子，没有配置 responseType，在我们内部没有自动加的情况下，由于没有配置 responseType，输出的 response 数据会是 json 字符串,而非 json 对象。
  method: 'post',
  url: '/base/post',
  data: {
    a: 1,
    b: 2
  }
}).then((res) => {
  console.log(res)
}).catch(e => {
  console.log(e)
})

axios({ // 这个例子手动配置了 responseType: 'json'，则 res 出去的 data 是 json 对象，而非 json 字符串
  method: 'post',
  url: '/base/post',
  responseType: 'json',
  data: {
    a: 3,
    b: 4
  }
}).then((res) => {
  console.log(res)
}).catch(e => {
  console.log(e)
})